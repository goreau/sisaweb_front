import axios from "@/services/api.js";

class MobAladoService {
  async sync(data) {
    try {
      const res = await axios.post("/api/mobile/syncAlado", data);
      return res.data;
    } catch (error) {
      if (error.response && error.response.data) {
        return error.response.data;
      } else {
        return { error: true, msg: 'Erro de comunicação com o servidor.' };
      }
    }
  }

  async getMobAlado(id) {
    try {
      const res = await axios.get(`/api/mobile/mobAlado/${id}`);
      return res.data;
    } catch (error) {
      if (error.response && error.response.data) {
        return error.response.data;
      } else {
        return { error: true, msg: 'Erro de comunicação com o servidor.' };
      }
    }
  }

  async update(data) {
    try {
      const res = await axios.put("/api/mobile/mobAlado", data);
      return res.data;
    } catch (error) {
      if (error.response && error.response.data) {
        return error.response.data;
      } else {
        return { error: true, msg: 'Erro de comunicação com o servidor.' };
      }
    }
  }

  async delete(id) {
    try {
      const res = await axios.delete(`/api/mobile/mobAlado/${id}`);
      return res.data;
    } catch (error) {
      if (error.response && error.response.data) {
        return error.response.data;
      } else {
        return { error: true, msg: 'Erro de comunicação com o servidor.' };
      }
    }
  }


  async getMobAlados(filter) {
    try {
      const res = await axios.get(`/api/mobile/mobAlados/${filter}`);
      return res.data;
    } catch (error) {
      if (error.response && error.response.data) {
        return error.response.data;
      } else {
        return { error: true, msg: 'Erro de comunicação com o servidor.' };
      }
    }
  }

}

export default new MobAladoService();
